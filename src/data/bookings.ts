export const getBookingById = (id: string): Booking | undefined => {
  return bookings.find((booking) => booking.id === id);
};

export const getBookingsByUserId = (userId: string): Booking[] => {
  return bookings.filter((booking) => booking.userId === userId);
};

export const getBookingsByStatus = (status: Booking["status"]): Booking[] => {
  return bookings.filter((booking) => booking.status === status);
};

export const getBookingsByServiceId = (serviceId: string): Booking[] => {
  return bookings.filter((booking) => booking.serviceId === serviceId);
};

export const getBookingsByProviderId = (providerId: string): Booking[] => {
  return bookings.filter((booking) => booking.providerId === providerId);
};

